using System;

public class lessonWorkingWithNumbers
{
    static public void Main ()
    {
        Console.WriteLine(Math.Abs(-40));//40
        Console.WriteLine(Math.Pow(3,2));//9
        Console.WriteLine(Math.Sqrt(9));//3
        Console.WriteLine(Math.Max(4,90));//90
        Console.WriteLine(Math.Min(4,90));//4
        Console.WriteLine(Math.Round(4.3));//4
        Console.WriteLine(Math.Round(4.5));//5

    }

}
